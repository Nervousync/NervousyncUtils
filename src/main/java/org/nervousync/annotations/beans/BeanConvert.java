package org.nervousync.annotations.beans;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Annotation for convert object/string between java bean and XML/JSON/YAML
 *
 * @author Steven Wee	<a href="mailto:wmkm0113@Hotmail.com">wmkm0113@Hotmail.com</a>
 * @version $Revision : 1.0 $ $Date: 8/15/2020 3:25 PM $
 */
@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.FIELD})
public @interface BeanConvert {

	/**
	 * Converter class array
	 *
	 * @return the class array
	 */
	Class<?>[] value() default {};

}
