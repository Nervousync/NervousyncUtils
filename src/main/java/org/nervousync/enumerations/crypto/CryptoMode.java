/**
 * The enum Crypto mode.
 */
public enum CryptoMode {
    /**
     * Encrypt crypto mode.
     */
    ENCRYPT,
    /**
     * Decrypt crypto mode.
     */
    DECRYPT,
    /**
     * Signature crypto mode.
     */
    SIGNATURE,
    /**
     * Verify crypto mode.
     */
    VERIFY
}
